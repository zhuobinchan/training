package com.ehighsun.training.pojo;

import java.util.ArrayList;
import java.util.List;

public class Person {
	
	/*
	 * accountNum:登录账号
	 * password:登录密码
	 * role:用户角色
	 * name:姓名
	 * telephone:电话	
	 * idCard:身份证
	 * gender:性别
	 * openId:微信openId
	 * headUrl:头像地址
	 * address:地址
	 * email:邮箱
	 * nickName:昵称
	 * */
	
	private Integer pid;
	
	private String accountNum;
	private String password;
	private String role;
	
	private String name;
	private String telephone;
	private String idCard;
	private String gender;
	private String openId;
	private String headUrl;
	private String address;
	private String email;
	private String nickName;
	private List<Story> stories = new ArrayList<>();
	
	public Person(Integer pid, String accountNum, String password, String role,
			String name, String telephone, String idCard, String gender,
			String openId, String headUrl, String address, String email,
			String nickName, List<Story> stories) {
		super();
		this.pid = pid;
		this.accountNum = accountNum;
		this.password = password;
		this.role = role;
		this.name = name;
		this.telephone = telephone;
		this.idCard = idCard;
		this.gender = gender;
		this.openId = openId;
		this.headUrl = headUrl;
		this.address = address;
		this.email = email;
		this.nickName = nickName;
		this.stories = stories;
	}
	public Person() {
		super();
	}
	public String getAccountNum() {
		return accountNum;
	}
	public void setAccountNum(String accountNum) {
		this.accountNum = accountNum;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTelephone() {
		return telephone;
	}
	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}
	public String getIdCard() {
		return idCard;
	}
	public void setIdCard(String idCard) {
		this.idCard = idCard;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getOpenId() {
		return openId;
	}
	public void setOpenId(String openId) {
		this.openId = openId;
	}
	public String getHeadUrl() {
		return headUrl;
	}
	public void setHeadUrl(String headUrl) {
		this.headUrl = headUrl;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public Integer getPid() {
		return pid;
	}
	public void setPid(Integer pid) {
		this.pid = pid;
	}
	public List<Story> getStories() {
		return stories;
	}
	public void setStories(List<Story> stories) {
		this.stories = stories;
	}
	

}
